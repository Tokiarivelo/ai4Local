import type { AbTest, VariantMetrics } from '../types';

const generateMetrics = (variantId: string, seed: number): VariantMetrics => {
  const impressions = 10000 + seed * 1000;
  const clicks = Math.floor(impressions * (0.02 + seed * 0.005));
  const conversions = Math.floor(clicks * (0.1 + seed * 0.02));

  return {
    variantId,
    impressions,
    clicks,
    conversions,
    ctr: (clicks / impressions) * 100,
    conversionRate: (conversions / clicks) * 100,
    cpa: 15.5 - seed * 0.5,
    ltv: 180 + seed * 20,
    engagement: 25 + seed * 5,
    confidence: 85 + seed * 2,
  };
};

